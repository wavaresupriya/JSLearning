// while loop programs
// WAP to print number from 5 to 15 by incrementing 1
var i = 5;
while (i<=15) {
    console.log(i);
    i++;
}
console.log(`--------------------------------`);
// WAP to print numbers from 50 to 40 by decrementing by 1
var i = 50;
while (i>=40) {
    console.log(i);
    i--;
}
console.log(`--------------------------------`);
